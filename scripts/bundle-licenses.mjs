#!/usr/bin/env node
// SPDX-License-Identifier: LicenseRef-FSL-1.1-Apache-2.0
// Generate THIRD_PARTY_LICENSES by aggregating license texts of every
// production runtime dependency reported by `pnpm licenses list --prod`.
//
// Output is a single Markdown file shipped inside the Docker image and any
// tarball release artifacts, satisfying attribution requirements for MIT,
// BSD, Apache-2.0, ISC, MPL-2.0, etc.

import { execSync } from "node:child_process";
import { existsSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { join, dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const scriptsDir = dirname(__filename);
const repoRoot = resolve(scriptsDir, "..");
const outFile = join(repoRoot, "THIRD_PARTY_LICENSES");
const overridesDir = join(scriptsDir, "license-overrides");

// Match LICENSE / LICENCE / COPYING / NOTICE and any common suffix variants
// (e.g. LICENSE.md, LICENSE-MIT, COPYING.txt). Apache-2.0 §4(d) requires
// NOTICE to be reproduced alongside LICENSE — so we capture *every* match
// rather than stopping at the first hit.
const LICENSE_FILE_RE = /^(licen[sc]e|copying|notice)/i;

function findLicenseFiles(pkgPath) {
  if (!pkgPath || !existsSync(pkgPath)) return [];
  const matches = readdirSync(pkgPath).filter((e) => LICENSE_FILE_RE.test(e));
  // Stable order: alphabetical, which conveniently puts LICENSE before NOTICE
  // (the conventional reading order for Apache-2.0 modules).
  matches.sort((a, b) => a.localeCompare(b));
  return matches.map((filename) => ({
    filename,
    text: readFileSync(join(pkgPath, filename), "utf8").trim(),
  }));
}

function loadOverride(pkgName) {
  // Filename convention: package name with `/` → `+`. See license-overrides/README.md.
  const slug = pkgName.replace(/\//g, "+");
  const path = join(overridesDir, `${slug}.txt`);
  if (!existsSync(path)) return null;
  return readFileSync(path, "utf8").trim();
}

const raw = execSync("pnpm licenses list --prod --long --json", {
  encoding: "utf8",
  cwd: repoRoot,
  maxBuffer: 64 * 1024 * 1024,
});
const data = JSON.parse(raw);

// Flatten { "<License>": [pkg, ...] } → unique [{name, version, license, path}]
const seen = new Map();
for (const [license, pkgs] of Object.entries(data)) {
  for (const pkg of pkgs) {
    const versions = pkg.versions ?? [pkg.version];
    const paths = pkg.paths ?? [pkg.path];
    for (let i = 0; i < versions.length; i++) {
      const key = `${pkg.name}@${versions[i]}`;
      if (seen.has(key)) continue;
      seen.set(key, {
        name: pkg.name,
        version: versions[i],
        license: pkg.license ?? license,
        path: paths[i] ?? paths[0],
        homepage: pkg.homepage,
        author: pkg.author,
      });
    }
  }
}

const sorted = [...seen.values()].sort((a, b) =>
  a.name === b.name ? a.version.localeCompare(b.version) : a.name.localeCompare(b.name),
);

const lines = [];
lines.push("# ShellWatch — Third-Party License Notices");
lines.push("");
lines.push(
  "This file aggregates the license texts of every production runtime dependency bundled with the ShellWatch Docker image and release artifacts.",
);
lines.push("");
lines.push(
  "Generated by `scripts/bundle-licenses.mjs` from `pnpm licenses list --prod --long --json`.",
);
lines.push(`Generated at: ${new Date().toISOString()}`);
lines.push(`Total packages: ${sorted.length}`);
lines.push("");
lines.push("---");
lines.push("");

const missing = [];
let overridden = 0;
for (const pkg of sorted) {
  lines.push(`## ${pkg.name}@${pkg.version}`);
  lines.push("");
  lines.push(`- **License:** ${pkg.license}`);
  if (pkg.homepage) lines.push(`- **Homepage:** ${pkg.homepage}`);
  if (pkg.author) {
    const authorStr = typeof pkg.author === "string" ? pkg.author : pkg.author.name;
    if (authorStr) lines.push(`- **Author:** ${authorStr}`);
  }
  lines.push("");

  let files = findLicenseFiles(pkg.path);
  let source = "package";
  if (files.length === 0) {
    const overrideText = loadOverride(pkg.name);
    if (overrideText) {
      files = [{ filename: "LICENSE", text: overrideText }];
      source = "override";
      overridden += 1;
    }
  }

  if (files.length > 0) {
    if (source === "override") {
      lines.push(
        `> Source: \`scripts/license-overrides/${pkg.name.replace(/\//g, "+")}.txt\` (upstream tarball ships no LICENSE file).`,
      );
      lines.push("");
    }
    // Sub-heading per file when multiple exist (e.g. LICENSE + NOTICE), so a
    // reader can tell them apart. Skip when there's only one — most packages.
    for (const file of files) {
      if (files.length > 1) {
        lines.push(`### ${file.filename}`);
        lines.push("");
      }
      lines.push("```");
      lines.push(file.text);
      lines.push("```");
      lines.push("");
    }
  } else {
    missing.push({ name: pkg.name, version: pkg.version, license: pkg.license });
  }
  lines.push("---");
  lines.push("");
}

if (missing.length > 0) {
  console.error(`\nERROR: ${missing.length} package(s) ship no LICENSE file and have no override:`);
  for (const m of missing) {
    console.error(`  - ${m.name}@${m.version}  (declared: ${m.license})`);
  }
  console.error(
    `\nFix by adding the upstream LICENSE text to scripts/license-overrides/<name>.txt`,
  );
  console.error(`(replace \`/\` in the package name with \`+\`). See:`);
  console.error(`  scripts/license-overrides/README.md`);
  process.exit(1);
}

// Non-npm bundled assets: fonts under client/static/fonts/ ship as woff2 in
// the build output but their source packages are devDeps, so `pnpm licenses
// list --prod` doesn't see them. Surface the OFL alongside so this file
// remains the single attribution stop.
const fontsLicense = join(repoRoot, "client", "static", "fonts", "OFL.txt");
let bundledFonts = 0;
if (existsSync(fontsLicense)) {
  lines.push("## Bundled fonts — Geist & Geist Mono");
  lines.push("");
  lines.push("- **License:** OFL-1.1");
  lines.push("- **Homepage:** https://github.com/vercel/geist-font");
  lines.push("");
  lines.push(
    "> Source: `client/static/fonts/OFL.txt` (also shipped at runtime alongside the woff2 files at `/fonts/`).",
  );
  lines.push("");
  lines.push("```");
  lines.push(readFileSync(fontsLicense, "utf8").trim());
  lines.push("```");
  lines.push("");
  lines.push("---");
  lines.push("");
  bundledFonts = 1;
}

writeFileSync(outFile, lines.join("\n"));

console.log(`Wrote ${outFile}`);
console.log(`  packages: ${sorted.length}`);
if (overridden > 0) console.log(`  via license-overrides/: ${overridden}`);
if (bundledFonts > 0) console.log(`  bundled fonts: ${bundledFonts}`);
